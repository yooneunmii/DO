package com.dev.doodle.vo;

public class Category {
	
	//멤버필드
	private int no;
	private String kind;
	
	//기본생성자
	public Category() {
		
	}

	//getter.setter
	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}
	
}
